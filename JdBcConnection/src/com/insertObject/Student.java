package com.insertObject;

public class Student 
{
int roll;
String name;
String branch;
public Student(int roll, String name, String branch) {
	super();
	this.roll = roll;
	this.name = name;
	this.branch = branch;
}
public int getRoll() {
	return roll;
}
public String getName() {
	return name;
}

public String getBranch() {
	return branch;
}


}
