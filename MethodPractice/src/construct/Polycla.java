package construct;

public class Polycla {
  public static void poly(int a) {
	
	switch(a){
    case 3:System.out.println("traingle");
    break;
    case 4:System.out.println("quadilateral");
        break;
   case 5:System.out.println("pentagon");
        break;
   case 6:System.out.println("hexagon");
        break;
   case 7:System.out.println("heptagon");
        break;
   case 8:System.out.println("octagon");
        break;
        
  case 9:System.out.println("nonagon");
        break;
      
  case 10:System.out.println("Decagon");
        break;
  case 11:System.out.println("hendacagon");
        break;
  case 12:System.out.println("dodecagon");
        break;
  case 13:System.out.println("tridecagon");
        break;
  case 14:System.out.println("tetradecagon");
        break;
        
  default:System.out.println("enter valid side");
}
}}
